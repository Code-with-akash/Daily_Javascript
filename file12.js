// booleans & Comparison operator

// booleans
// true , false

let num1 = 12;
let num2 = "12";

console.log(num1 >= num2)  // it will give boolean value as true.

// == vs === 

console.log(num1 == num2)  // this will give true because it will only check the given values but not data type.

console.log(num1 === num2) // this will be false.

// != vs !==

console.log(num1 != num2); // false....
console.log(num1 !== num2); // True....